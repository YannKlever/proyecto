function openModal(){
    document.getElementById("Lp").style.display="block";

}
function closeModal(){
    document.getElementById("Lp").style.display="none";
}
window.onclick = function(event){
    const departamento= document.getElementById("Lp");
    if (event.target ==departamento)
    departamento.style.display="none";
}
